	SUBROUTINE ASU_KUIP_INIT()

	INTEGER*4 PAW
	INTEGER*4 NWORDS

	COMMON/PAWC/PAW(500000)

	CALL MZEBRA(-3)
	CALL MZPAW(500000,' ')

	NWORDS=20000
	CALL KUINIT(NWORDS)

	CALL VECDEF       ! VECTOR commands
	
#ifdef MOTIF
	CALL KUIDFM       ! MOTIF commands
#endif

	CALL KUEXEC('PROMPT ''Staf[]''')

!	WRITE(*,*)'MY_KUIP_INIT successfully completed'

	RETURN
	END
